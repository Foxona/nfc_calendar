function addHourToDate(dateArray) {
  // Adjust the month for JavaScript's zero-based indexing
  dateArray[1] -= 1;

  const date = new Date(Date.UTC(...dateArray));
  date.setHours(date.getHours() + 1);

  // Correct the month when converting back
  return [
    date.getUTCFullYear(),
    date.getUTCMonth() + 1, // Correcting month for human-readable format
    date.getUTCDate(),
    date.getUTCHours(),
    date.getUTCMinutes(),
  ];
}

function convertEventToIcsFormat(event) {
  const startTime = event.placement.startTime;
  const endTime = event.placement.endTime;

  // Parse the ISO string to a date array, adjusting for month indexing
  const startArray = [
    parseInt(startTime.substring(0, 4), 10),
    parseInt(startTime.substring(5, 7), 10),
    parseInt(startTime.substring(8, 10), 10),
    parseInt(startTime.substring(11, 13), 10),
    parseInt(startTime.substring(14, 16), 10),
  ];
  const endArray = [
    parseInt(endTime.substring(0, 4), 10),
    parseInt(endTime.substring(5, 7), 10),
    parseInt(endTime.substring(8, 10), 10),
    parseInt(endTime.substring(11, 13), 10),
    parseInt(endTime.substring(14, 16), 10),
  ];

  return {
    start: addHourToDate(startArray),
    end: addHourToDate(endArray),
    title: event.title,
    description: event.description,
    location: event.placement.roomName,
    uid: event.id,
  };
}

const deduplicateEvents = events.reduce((acc, current) => {
  const duplicatedItem = acc.find(
    (item) =>
      item.title === current.title &&
      item.placement.startTime === current.placement.startTime &&
      item.placement.endTime === current.placement.endTime
  );

  if (!duplicatedItem) {
    acc.push(current);
    return acc;
  } else {
    duplicatedItem.placement.roomName = `${duplicatedItem.placement.roomName} | ${current.placement.roomName}`;
    return acc;
  }
}, []);

const icsEvents = deduplicateEvents.map(convertEventToIcsFormat);

ics.createEvents(icsEvents, (error, value) => {
  if (error) {
    console.log("Error creating ICS:", error);
    return;
  }

  fs.writeFileSync("events.ics", value);
});
